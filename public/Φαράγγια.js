function convertToGreekLatin(str) {
    const conversionMap = {
        
        'a': 'α',
        'b': 'β',
        'c': 'κ',
        'd': 'δ',
        'e': 'ε',
        'f': 'φ',
        'g': 'γ',
        'h': 'η',
        'i': 'ι',
        'j': 'τζ',
        'k': 'κ',
        'l': 'λ',
        'm': 'μ',
        'n': 'ν',
        'o': 'ο',
        'p': 'π',
        'q': 'κ',
        'r': 'ρ',
        's': 'σ',
        't': 'τ',
        'u': 'υ',
        'v': 'β',
        'w': 'ω',
        'x': 'ξ',
        'y': 'ψ',
        'z': 'ζ',        
        'th': 'θ',
        'y': 'υ',
        'x': 'χ',

    };

    return str.replace(/[a-z]/g, char => conversionMap[char] || char);
}

function removeDiacritics(word) {
    return word.normalize("NFD").replace(/[\u0300-\u036f]/g, "").replace(/ά/g, 'α').replace(/έ/g, 'ε').replace(/ή/g, 'η').replace(/ί/g, 'ι').replace(/ό/g, 'ο').replace(/ύ/g, 'υ').replace(/ώ/g, 'ω');
}


