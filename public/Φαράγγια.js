function sortTable() {
    // Sort only by the canyon name (second column) while keeping original indexes
    let sortedData = canyonsData.map(row => row.slice()) // Clone data to avoid modifying original
        .sort((a, b) => a[1].localeCompare(b[1])); // Sort by canyon name

    // Reassign sorted names and remaining columns back to their original row numbers
    canyonsData.forEach((row, index) => {
        row[1] = sortedData[index][1]; // Assign sorted canyon name
        row[2] = sortedData[index][2]; // Assign sorted region
        row[3] = sortedData[index][3]; // Assign sorted mountain
    });
}

function populateTable() {
    const tableBody = document.getElementById("table-body-canyons");
    tableBody.innerHTML = "";

    // Sort before populating the table
    sortTable();

    // Populate the table
    canyonsData.forEach(row => {
        let tr = document.createElement("tr");

        row.forEach(cell => {
            let td = document.createElement("td");
            td.textContent = cell;
            tr.appendChild(td);
        });

        tableBody.appendChild(tr);
    });
}

